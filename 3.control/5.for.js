//반복문 제어 : continue , break
for (let i = 0; i < 20; i++) {
  if (i === 10) {
    continue; // continue 가나오면 아래의 그 어떤코드든 무시가되고, 바로 다음으로 넘어감 
    console.log('i가 드디어 10이 되었다!');
    break; // 특정조건 만족할때 반복문을 멈춤
  }
  console.log(i);
}
